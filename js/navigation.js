// Show navigation
function showNav() {
    navigation.style.width = "214px";
    openNav.style.display = "none";
    closeNav.style.display = "flex";
}

// Hide navigation
function hideNav() {
    navigation.style.width = "0";
    navigation.style.padding = "0";
    closeNav.style.display = "none";
    openNav.style.display = "flex";
}
